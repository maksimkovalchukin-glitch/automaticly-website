// ===== STATE =====
let lang = localStorage.getItem('lang') || 'uk';

// ===== APPLY TRANSLATIONS =====
function t(key) { return T[lang][key] || T['uk'][key] || key; }

function applyLang(l) {
  lang = l;
  localStorage.setItem('lang', l);
  document.documentElement.lang = l;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.dataset.i18n;
    if (T[l][k] !== undefined) el.innerHTML = T[l][k];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.dataset.i18nPh;
    if (T[l][k] !== undefined) el.placeholder = T[l][k];
  });
  document.querySelectorAll('[data-i18n-val]').forEach(el => {
    const k = el.dataset.i18nVal;
    if (T[l][k] !== undefined) el.value = T[l][k];
  });
  const btn = document.getElementById('langBtn');
  if (btn) btn.textContent = l === 'uk' ? 'EN' : 'UA';
}

// ===== NAV ACTIVE =====
function setNavActive() {
  const path = window.location.pathname.replace(/\/index\.html$/, '/');
  document.querySelectorAll('[data-nav]').forEach(a => {
    a.classList.remove('active');
    const href = a.getAttribute('href') || '';
    const pg = a.dataset.nav;
    if (
      (pg === 'home' && (path.endsWith('/') || path.endsWith('index.html'))) ||
      (pg !== 'home' && path.includes(pg))
    ) a.classList.add('active');
  });
}

// ===== MOBILE MENU =====
function initMobile() {
  const burger = document.getElementById('burger');
  const menu = document.getElementById('mobileMenu');
  if (!burger || !menu) return;
  burger.addEventListener('click', () => menu.classList.toggle('open'));
  menu.querySelectorAll('a').forEach(a => a.addEventListener('click', () => menu.classList.remove('open')));
}

// ===== NAV SCROLL =====
function initNavScroll() {
  const nav = document.getElementById('nav');
  if (!nav) return;
  window.addEventListener('scroll', () => {
    nav.style.background = window.scrollY > 20 ? 'rgba(10,13,18,.98)' : 'rgba(10,13,18,.85)';
  }, { passive: true });
}

// ===== REVEAL ON SCROLL =====
function initReveal() {
  const obs = new IntersectionObserver(
    entries => entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); }),
    { threshold: 0.12 }
  );
  document.querySelectorAll('.reveal').forEach((el, i) => {
    el.style.transitionDelay = `${(i % 4) * 0.1}s`;
    obs.observe(el);
  });
}

// ===== SMOOTH SCROLL =====
function initSmooth() {
  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', e => {
      const target = document.querySelector(a.getAttribute('href'));
      if (!target) return;
      e.preventDefault();
      window.scrollTo({ top: target.offsetTop - 80, behavior: 'smooth' });
    });
  });
}

// ===== FAQ ACCORDION =====
function initFaq() {
  document.querySelectorAll('.faq__item').forEach(item => {
    const btn = item.querySelector('.faq__q');
    if (!btn) return;
    btn.addEventListener('click', () => {
      const isOpen = item.classList.contains('open');
      document.querySelectorAll('.faq__item.open').forEach(o => o.classList.remove('open'));
      if (!isOpen) item.classList.add('open');
    });
  });
}

// ===== FILTER =====
function initFilter(gridSel, cardAttr) {
  const bar = document.querySelector('.filter-bar');
  const grid = document.querySelector(gridSel);
  if (!bar || !grid) return;
  bar.querySelectorAll('.filter-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      bar.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      const cat = btn.dataset.filter;
      grid.querySelectorAll(`[${cardAttr}]`).forEach(card => {
        card.classList.toggle('hidden', cat !== 'all' && card.getAttribute(cardAttr) !== cat);
      });
    });
  });
}

// ===== LANG SWITCHER =====
function initLangSwitcher() {
  document.querySelectorAll('.lang-switcher').forEach(btn => {
    btn.addEventListener('click', () => applyLang(lang === 'uk' ? 'en' : 'uk'));
  });
}

// ===== CONTACT FORM =====
function initContactForm() {
  const form = document.getElementById('contactForm');
  if (!form) return;
  form.addEventListener('submit', async e => {
    e.preventDefault();
    const btn = form.querySelector('[type=submit]');
    const success = document.getElementById('formSuccess');
    btn.disabled = true;
    btn.textContent = '...';
    try {
      const res = await fetch(form.action, {
        method: 'POST', body: new FormData(form),
        headers: { 'Accept': 'application/json' }
      });
      if (res.ok) {
        form.reset();
        if (success) success.style.display = 'block';
      }
    } catch {}
    btn.disabled = false;
    applyLang(lang);
  });
}

